// maak een variabele aan (= declareren):
let name = 'Olmo'


// nu kunnen we deze variabele gebruiken:
console.log('Hello ' + name)


// we kunnen een variable een nieuwe waarde geven:
name = 'Anna'
console.log('Hello ' + name)


// er zijn drie manieren om een variabele te declareren.
